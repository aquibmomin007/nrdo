            <h3>Data Management, Data Analysis and Reporting</h3>
            <p>A team of data management staff worked with the vendor to develop the National Disease Registry System to capture data from various sources. This team not only manages the database to ensure that all data collected for the registries have been validated and are properly anonymised, but also extracts data for analysis and reporting.</p>
            <p>The team of epidemiologists and biostatistician analyses the data and generates reports for MOH on a regular basis. The analysis is performed on anonymised data only and the reports contain aggregate data and trends on incidence, prevalence and mortality of the disease covered by the particular registry.</p>

            <h3>Quality Assurance</h3>
            <p>The team of quality assurance executives ensures that requests for data go through the proper level of approval and data is being released according to the guidelines of the National Registry of Diseases Act. In addition, the team develops quality assurance policies and processes together with the registry staff in order to ensure registry output of the highest standards. The executives also perform regular audits of the sites and systems, analyse audit data and present the results in a timely and succinct manner to the management of Health Promotion Board (HPB). External audits are conducted by vendors contracted by HPB.</p>

            <h3>Registry Committees</h3>
            <p>For each registry, Ministry of Health (MOH) appoints a registry committee which comprises clinicians and epidemiologists from various public and private healthcare sectors. The roles of the registry committees are to:</p>
            <ul>
                <li>Provide professional support to National Registry of Diseases Office (NRDO)
                <li>Promote and facilitate submission of relevant data to NRDO by healthcare professionals and institutions</li>
                <li>Advise on the production of annual registry report<</li>
                <li>Ensure reports required by MOH are submitted timely and in appropriate format</li>
                <li>Advise and participate in the development and enhancement of the registry system</li>
                <li>Promote the use of the registry.</li>
            </ul>

