            <h3>Data Collection & Entry</h3>
            <p>Data collection and entry are carried out by the registry coordinators under the supervision of the Field Administrator. New cases are identified by NRDO through various sources including:</p>
            <ul>
                <li>Notifications by healthcare professionals
                <li>Hospital data for cases admitted to hospitals
                <li>Pathology and laboratory reports</li>
            </ul>
